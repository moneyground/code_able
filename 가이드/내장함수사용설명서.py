#자주쓰는 내장함수

# abs(x)  정수/실수형 값 x를 입력받고 해당 값의 절댓값을 반환합니다 
# all(iterable)  iterable이 모두 참이면 True, 거짓이 하나라도 있으면 False를 반환합니다.
# any(iterable)  iterable 중 하나라도 참이 있을 경우 True를 반환합니다.
# chr(x)  유니코드 x를 입력받고 그 값에 해당하는 문자열을 반환합니다.
# enumerate(iterable)  이터러블을 입력받아 인덱스와 원소로 이루어진 튜플 객체 enumerate를 반환합니다.
# id(object)  객체를 입력받아 해당 객체의  고윳값을 반환합니다. 고윳값은 객체가 할당된 메모리의 주솟값을 의미합니다. 
# len(iterable)  이터러블을 입력받아 해당 객체의 원소 개수를 반환합니다.
# list()  문자열이나 튜플을 입력받아 리스트 객체를 만들어 반환합니다.
# max(x1, x2, ... , xn) 	x1, x2, ... , xn 사이에서 가장 큰 수를 반환합니다.
# min(x1, x2, ... , xn) 	x1, x2, ... , xn 사이에서 가장 작은 수를 반환합니다.
# sorted()  입력값을 정렬한 후 정렬된 결괏값을 리스트로 반환합니다.
# zip(iterable, iterable)   iterable들의 index 순서에 따라서 묶어 튜플을 반환합니다.